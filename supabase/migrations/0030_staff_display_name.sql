-- =============================================================
-- Phase 28 F案: 職員の表示名（送迎表の担当セル用の短縮名）
--
-- 送迎表の担当ドロップダウンは幅を抑えたいが、フルネームだと
-- 「濱田 亜希子」のように長くなり、同職員が担当するエリアマーク
-- が隠れてしまう問題があった。ここで手動登録できる 3 文字まで
-- の短い表示名を持たせ、送迎表の select ラベルに使う。
--
-- 未登録時は app 側で name の先頭 2 文字をフォールバック表示する
-- （DB 制約なし。3 文字上限は app バリデーションのみ）。
-- =============================================================

alter table public.staff
  add column if not exists display_name text;

comment on column public.staff.display_name is
  '送迎表の担当セル用の短縮表示名（最大 3 文字、app バリデーション）。未登録なら name の先頭 2 文字にフォールバック。Phase 28 F案追加。';
